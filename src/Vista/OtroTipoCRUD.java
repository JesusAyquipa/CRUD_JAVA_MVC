    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Date fechaNacimiento = jDateFechaNacimiento.getDate();
        Persona registrarPersona = new Persona();
        PersonaControlador nuevaPersona = new PersonaControlador();
        
        _nombre = txtNombre.getText();
        _apellido = txtApellido.getText();
        _correo = txtCorreo.getText();
        _pais = txtPais.getText();
        _profesion = txtProfesion.getText();
        
        switch(cmbRoles.getSelectedIndex()){
            case 0:
            rol= 1;
            break;
            case 1:
            rol= 2;
            break;
            case 2:
            rol= 3;
            break;
            case 3:
            rol= 4;
            break;
            default:
                rol = 2;
        }
        
        registrarPersona.setNombre(_nombre);
        registrarPersona.setApellido(_apellido);
        registrarPersona.setCorreo(_correo);
        registrarPersona.setPais(_pais);
        registrarPersona.setProfesion(_profesion);
        registrarPersona.setRol_id(rol);
        registrarPersona.setFecha_nacimiento(fechaNacimiento);
        
        nuevaPersona.creacion(registrarPersona);
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        String idPersona = txtBuscarPersona.getText().trim();
        if(idPersona.equals("")){
            JOptionPane.showMessageDialog(null, "Error al tratar de capturar ID", "Debes ingresar un ID", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Date fechaNacimiento = jDateFechaNacimiento.getDate();
        Persona actualizarPersona = new Persona();
        PersonaControlador edicionPersona = new PersonaControlador();

        _nombre = txtNombre.getText();
        _apellido = txtApellido.getText();
        _correo = txtCorreo.getText();
        _pais = txtPais.getText();
        _profesion = txtProfesion.getText();

        switch(cmbRoles.getSelectedIndex()){
            case 0:
            rol= 1;
            break;
            case 1:
            rol= 2;
            break;
            case 2:
            rol= 3;
            break;
            case 3:
            rol= 4;
            break;
            default:
            rol = 2;
        }

        actualizarPersona.setNombre(_nombre);
        actualizarPersona.setApellido(_apellido);
        actualizarPersona.setCorreo(_correo);
        actualizarPersona.setPais(_pais);
        actualizarPersona.setProfesion(_profesion);
        actualizarPersona.setRol_id(rol);
        actualizarPersona.setFecha_nacimiento(fechaNacimiento);
        edicionPersona.actualizar(actualizarPersona, Integer.parseInt(idPersona));
        
        JOptionPane.showMessageDialog(null, "Persona actualizada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        txtBuscarPersona.setEditable(false);
        String idPersona = txtBuscarPersona.getText();
        if(idPersona.equals("")){
            JOptionPane.showMessageDialog(null, "Error al tratar de eliminar", "Debes ingresar un ID", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PersonaControlador controladorEliminar = new PersonaControlador();
        controladorEliminar.eliminar(Integer.parseInt(idPersona));

    }//GEN-LAST:event_btnBorrarActionPerformed

