    private void limpiarCamposFormulario(){
            txtApellido.setText("");
            txtCorreo.setText("");
            txtNombre.setText("");
            txtPais.setText("");
            txtProfesion.setText("");
            cmbRoles.setSelectedIndex(0);
            txtBuscarPersona.setEditable(true);
            Date fechaActual = new Date();
            jDateFechaNacimiento.setDate(fechaActual);
    }
    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        txtBuscarPersona.setEditable(false);
        String idPersona = txtBuscarPersona.getText();
        if(idPersona.equals("")){
            JOptionPane.showMessageDialog(null, "Error al tratar de buscar", "Debes ingresar un ID", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PersonaControlador controladorBuscar = new PersonaControlador();
        Persona buscarPersona = controladorBuscar.lectura(Integer.parseInt(idPersona));

        if(buscarPersona != null){
            txtApellido.setText(buscarPersona.getApellido());
            txtCorreo.setText(buscarPersona.getCorreo());
            txtNombre.setText(buscarPersona.getNombre());
            txtPais.setText(buscarPersona.getPais());
            txtProfesion.setText(buscarPersona.getProfesion());

            switch (buscarPersona.getRol_id()) {
                case 1:
                cmbRoles.setSelectedIndex(0);
                break;
                case 2:
                cmbRoles.setSelectedIndex(1);
                break;
                case 3:
                cmbRoles.setSelectedIndex(2);
                break;
                case 4:
                cmbRoles.setSelectedIndex(3);
                break;
            }

            jDateFechaNacimiento.setDate(buscarPersona.getFecha_nacimiento());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

